/** \file int_types.h
 * \brief The fprintf() PRI[d,u,x,o,i,X][8,16,32] macros for 32 bits signed and unsigned integers.
 *
 */

#ifndef INT_TYPES_H
#define INT_TYPES_H

#define PRId8 "hd"
#define PRId16 "d"
#define PRId32 "ld"
#define PRIu8 "hu"
#define PRIu16 "u"
#define PRIu32 "lu"
#define PRIx8 "hx"
#define PRIx16 "x"
#define PRIx32 "lx"
#define PRIo8 "ho"
#define PRIo16 "o"
#define PRIo32 "lo"
#define PRIi8 "hi"
#define PRIi16 "i"
#define PRIi32 "li"
#define PRIX8 "hX"
#define PRIX16 "X"
#define PRIX32 "lX"

#endif
